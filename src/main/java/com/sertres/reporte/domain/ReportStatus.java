public class ReportStatus {

    private int ReportStatusId;

    private String ReportStatusDesc;

    public int getReportStatusId() {
        return ReportStatusId;
    }

    public void setReportStatusId(int reportStatusId) {
        ReportStatusId = reportStatusId;
    }

    public String getReportStatusDesc() {
        return ReportStatusDesc;
    }

    public void setReportStatusDesc(String reportStatusDesc) {
        ReportStatusDesc = reportStatusDesc;
    }
}
