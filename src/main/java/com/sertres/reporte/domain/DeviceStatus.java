package com.sertres.reporte.domain;

public class DeviceStatus {

    private int deviceStatusId;

    private String deviceStatusDescription;

    public int getDeviceStatusId() {
        return deviceStatusId;
    }

    public void setDeviceStatusId(int deviceStatusId) {
        this.deviceStatusId = deviceStatusId;
    }

    public String getDeviceStatusDescription() {
        return deviceStatusDescription;
    }

    public void setDeviceStatusDescription(String deviceStatusDescription) {
        this.deviceStatusDescription = deviceStatusDescription;
    }
}
